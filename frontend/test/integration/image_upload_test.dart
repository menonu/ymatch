@Tags(['integration'])
library;

import 'dart:convert';
import 'dart:io';

import 'package:flutter_test/flutter_test.dart';

void main() {
  group('Image upload integration (real backend)', () {
    test('upload PNG image returns valid URL', () async {
      final pngBytes = _createMinimalPng();
      final url = await _uploadImage(pngBytes, 'test_image.png');

      expect(url, isNotEmpty);
      expect(url, contains('uploads/'));
      expect(url, endsWith('.png'));

      final fullUrl = url.startsWith('http')
          ? url
          : 'http://localhost:3000/$url';
      final uri = Uri.parse(fullUrl);
      final client = HttpClient();
      final request = await client.getUrl(uri);
      final response = await request.close();
      expect(response.statusCode, 200);
      client.close();
    });

    test('upload JPG image returns valid URL', () async {
      final jpgBytes = _createMinimalJpg();
      final url = await _uploadImage(jpgBytes, 'test_image.jpg');

      expect(url, isNotEmpty);
      expect(url, endsWith('.jpg'));
    });

    test('upload rejects non-image content-type', () async {
      final textBytes = utf8.encode('this is not an image');

      expect(
        () async => await _uploadImageRaw(textBytes, 'fake.dat', 'text/plain'),
        throwsA(isA<Exception>()),
      );
    });

    test('uploaded image is accessible and has correct content-type', () async {
      final pngBytes = _createMinimalPng();
      final url = await _uploadImage(pngBytes, 'verify_test.png');

      final fullUrl = url.startsWith('http')
          ? url
          : 'http://localhost:3000/$url';
      final uri = Uri.parse(fullUrl);
      final client = HttpClient();
      final request = await client.getUrl(uri);
      final response = await request.close();

      expect(response.statusCode, 200);
      final contentType = response.headers.value('content-type');
      expect(contentType, contains('image'));
      client.close();
    });
  });
}

Future<String> _uploadImage(List<int> bytes, String filename) async {
  final ext = filename.split('.').last.toLowerCase();
  final contentType = switch (ext) {
    'jpg' || 'jpeg' => 'image/jpeg',
    'png' => 'image/png',
    'gif' => 'image/gif',
    'webp' => 'image/webp',
    _ => 'image/png',
  };
  return _uploadImageRaw(bytes, filename, contentType);
}

Future<String> _uploadImageRaw(
  List<int> bytes,
  String filename,
  String contentType,
) async {
  final boundary = '----TestBoundary${DateTime.now().millisecondsSinceEpoch}';
  final uri = Uri.parse('http://localhost:3000/api/v1/images/upload');

  final body = <int>[];
  void addString(String s) => body.addAll(utf8.encode(s));
  void addBytes(List<int> b) => body.addAll(b);

  addString('--$boundary\r\n');
  addString(
    'Content-Disposition: form-data; name="file"; filename="$filename"\r\n',
  );
  addString('Content-Type: $contentType\r\n');
  addString('\r\n');
  addBytes(bytes);
  addString('\r\n--$boundary--\r\n');

  final client = HttpClient();
  try {
    final request = await client.postUrl(uri);
    request.headers.set(
      'Content-Type',
      'multipart/form-data; boundary=$boundary',
    );
    request.contentLength = body.length;
    request.add(body);

    final response = await request.close();
    final responseBody = await response.transform(utf8.decoder).join();

    if (response.statusCode >= 200 && response.statusCode < 300) {
      final json = jsonDecode(responseBody);
      return json['url'] as String;
    } else {
      throw Exception('HTTP ${response.statusCode}: $responseBody');
    }
  } finally {
    client.close();
  }
}

